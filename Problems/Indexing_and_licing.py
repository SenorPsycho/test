# Indexing and Slicing
#    Given the string s = "PythonPractice", what are the outputs of:
#    - s[1]
#    - s[-3:]
#    - s[2:7]

Given_String = "PythonPractice"

# Outputs
print(Given_String[1])    # Output: 'y'
print(Given_String[-3:])  # Output: 'ice'
print(Given_String[2:7])  # Output: 'thonP'


